//2. Write a program in C to separate individual characters from a string

#include<stdio.h>
#include<string.h>
int main(){
	int i;
	char str[100];
	printf("Enter a String : ");
	scanf("%s",&str[i]);
	
	printf("\nSeparated Characters of string : \n");
	for(i=0;i<=str[i];i++){
		printf("%c\n",str[i]);
		
	}
}
